# Collection of Items
# It can be of different data types

# List with only String data type
fruit_names = ["Oranges", "Apple", "Kiwi", "Watermelon"]
print("print list length: ", len(fruit_names))
print("print list 0 index item: ", len(fruit_names[0]))
print("print list 2 index item: ", fruit_names[2])
print("print list last index item: ", fruit_names[-1])

# list with different data types like (Integer, Boolean, Float, String)
list1 = [1, 2, 3, 4, True, 3.14, "Mohit"]
print("print list1 variable data type: ", type(list1))  # <class 'list'>
print("print list1 variable length: ", len(list1))  # <class 'list'>
