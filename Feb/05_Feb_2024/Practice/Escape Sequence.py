# Escape Sequence Examples:

# \n -> new ine
print("a\nb")
print("a\tb")
# \a - bell
# \t - tab char
# \f - formfeed


# Row Escape using 'r'
dir_path = r'C:\Program Files\PythonProject'
print("print row escape string: ", dir_path)

# Bell Escape using '\a'
